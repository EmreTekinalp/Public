def ring(render=OpenMayaRender.MGL_LINE_STRIP):
    """Create a ring shape."""
    for size in range(2):
        gl_ft.glBegin(render)
        for i in range(256):
            x = math.cos(i * 2 * PI / 128)
            z = math.sin(i * 2 * PI / 128)
            gl_ft.glVertex3f(x / float('1.%d' % size), 0,
                             z / float('1.%d' % size))
        # end for iterate range
        gl_ft.glEnd()
    # end for iterate range
# end def ring



