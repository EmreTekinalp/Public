    def draw(self, view, path, style, status):
        '''draw method'''
        node = OpenMaya.MFnDependencyNode(self.thisMObject())

        #--- get the size value
        plug = OpenMaya.MPlug(self.thisMObject(), CustomLocator.aSize)
        size = plug.asFloat()

        #--- get the orientation value
        plug = OpenMaya.MPlug(self.thisMObject(), CustomLocator.aOrientation)
        ori = plug.asFloat()

        #--- get the color value
        plug = OpenMaya.MPlug(self.thisMObject(), CustomLocator.aColor)
        color = plug.asShort()

        #--- get the width value
        plug = OpenMaya.MPlug(self.thisMObject(), CustomLocator.aWidth)
        width = plug.asFloat()

        #--- get the shape value
        plug = OpenMaya.MPlug(self.thisMObject(), CustomLocator.aShape)
        shape = plug.asFloat()

        view.beginGL()
        #--- push current state
        glFT.glPushAttrib(OpenMayaRender.MGL_CURRENT_BIT)
        #--- enable blend mode
        glFT.glEnable(OpenMayaRender.MGL_BLEND)
        #--- define blend mode
        glFT.glBlendFunc(OpenMayaRender.MGL_SRC_ALPHA, 
                         OpenMayaRender.MGL_ONE_MINUS_SRC_ALPHA)

        #--- define the size of the locator
        glFT.glScalef(size, size, size)

        #--- define the colors of the current state
        if (status == OpenMayaUI.M3dView.kLead):
                view.setDrawColor( 18, OpenMayaUI.M3dView.kActiveColors )

        elif (status == OpenMayaUI.M3dView.kActive):
                view.setDrawColor( 15, OpenMayaUI.M3dView.kActiveColors )

        elif (status == OpenMayaUI.M3dView.kActiveAffected):
                view.setDrawColor( 8, OpenMayaUI.M3dView.kActiveColors )

        elif (status == OpenMayaUI.M3dView.kDormant):
                view.setDrawColor( color, OpenMayaUI.M3dView.kActiveColors )

        elif (status == OpenMayaUI.M3dView.kHilite):
                view.setDrawColor( 17, OpenMayaUI.M3dView.kActiveColors )

        #--- translate the shape
        lx = cmds.getAttr(node.name() + '.localPositionX')
        ly = cmds.getAttr(node.name() + '.localPositionY')
        lz = cmds.getAttr(node.name() + '.localPositionZ')
        glFT.glTranslatef(lx,ly,lz)

        #--- rotate the shape
        if ori == 1:
            glFT.glRotatef(90,0,1,0)
        elif ori == 2:
            glFT.glRotatef(-90,0,1,0)
        elif ori == 3:
            glFT.glRotatef(90,1,0,0)
        elif ori == 4:
            glFT.glRotatef(-90,1,0,0)
        elif ori == 5:
            glFT.glRotatef(90,0,0,1)
        elif ori == 6:
            glFT.glRotatef(-90,0,0,1)
        elif ori == 7:
            glFT.glRotatef(180,0,-1,0)
        elif ori == 8:
            glFT.glRotatef(-180,1,0,0)
        elif ori == 9:
            glFT.glRotatef(180,-1,-1,0)
        elif ori == 10:
            glFT.glRotatef(-180,-1,0,0)
        elif ori == 11:
            glFT.glRotatef(180,0,0,-1)
        elif ori == 12:
            glFT.glRotatef(180,0,0,1)

        #--- define the width of the locator
        glFT.glLineWidth(width)

        #--- draw the shape
        self.shapes(shape = shape)

        #--- disable blend mode
        glFT.glDisable(OpenMayaRender.MGL_BLEND)
        #--- restore the state
        glFT.glPopAttrib()

        view.endGL()
